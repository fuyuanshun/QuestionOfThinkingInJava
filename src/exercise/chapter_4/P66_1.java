package exercise.chapter_4;

/**
 * 第四章 控制执行流程
 * P.66
 * 练习1. 写一个程序，打印从1到100的值
 */
public class P66_1 {
    public static void main(String[] args) {
        for (int i = 1; i < 101; i++) {
            System.out.println(i);
        }
    }
}/* Output:
        1
        2
        3
        4
        ...
        100
 */